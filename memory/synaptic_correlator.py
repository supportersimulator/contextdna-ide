def get_correlator() -> SynapticCorrelator:
    """Get singleton correlator instance."""
    if not hasattr(get_correlator, "_instance"):
        get_correlator._instance = SynapticCorrelator()
    return get_correlator._instance


def quick_analyze() -> Dict:
    """Quick analysis for webhook injection."""
    correlator = get_correlator()
    result = correlator.analyze()
    return {
        "insight_count": len(result.insights),
        "critical": len(result.get_critical()),
        "actionable": len(result.get_actionable()),
        "top_insight": result.insights[0].to_dict() if result.insights else None,
        "health": result.health_summary,
    }


def correlate_message(message: str) -> List[Dict]:
    """Correlate a message with system state."""
    correlator = get_correlator()
    insights = correlator.correlate_dialogue(message)
    return [i.to_dict() for i in insights]


