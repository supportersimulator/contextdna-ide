    def _analyze_dialogue(self) -> List[SynapticInsight]:
        """Analyze dialogue for insights."""
        insights = []

        try:
            from memory.dialogue_mirror import DialogueMirror
            mirror = DialogueMirror()
            context = mirror.get_context_for_synaptic(max_messages=10, max_age_hours=2)

            messages = context.get("dialogue_context", [])

            # Check for repeated topics (indicates focus area)
            topic_counts = {}
            for msg in messages:
                content = msg.get("content", "").lower()
                for topic in ["docker", "voice", "webhook", "error", "fix", "deploy"]:
                    if topic in content:
                        topic_counts[topic] = topic_counts.get(topic, 0) + 1

            # Insight: Heavy focus on one topic
            for topic, count in topic_counts.items():
                if count >= 3:
                    insights.append(SynapticInsight(
                        type=InsightType.OBSERVATION,
                        priority=InsightPriority.LOW,
                        title=f"Focus area: {topic}",
                        description=f"'{topic}' mentioned {count} times in recent dialogue",
                        sources=["dialogue_mirror"],
                        evidence={"topic": topic, "count": count},
                    ))

            # Check for error mentions
            error_count = sum(1 for m in messages if "error" in m.get("content", "").lower())
            if error_count >= 2:
                insights.append(SynapticInsight(
                    type=InsightType.CORRELATION,
                    priority=InsightPriority.MEDIUM,
                    title="Multiple errors discussed",
                    description=f"{error_count} messages mention errors",
                    sources=["dialogue_mirror"],
                    evidence={"error_mentions": error_count},
                    action="Review recent error patterns",
                ))

        except Exception as e:
            logger.debug(f"Dialogue analysis failed: {e}")

        return insights

    def _analyze_containers(self) -> List[SynapticInsight]:
        """Analyze container health for insights."""
        insights = []

        try:
            from memory.synaptic_observatory import get_observatory
            obs = get_observatory()
            awareness = obs.get_awareness()

            # Check for unhealthy containers
            unhealthy = [
                name for name, status in awareness.containers.items()
                if status.health == "unhealthy"
            ]

            if unhealthy:
                insights.append(SynapticInsight(
                    type=InsightType.ANOMALY,
                    priority=InsightPriority.HIGH,
                    title=f"{len(unhealthy)} unhealthy containers",
                    description=f"Containers reporting unhealthy: {', '.join(unhealthy)}",
                    sources=["docker_health"],
                    evidence={"unhealthy": unhealthy},
                    action=f"docker restart {unhealthy[0]}" if len(unhealthy) == 1 else "Review container health",
                ))

            # Check for missing critical containers
            critical = ["contextdna-pg", "contextdna-redis", "contextdna-helper-agent"]
            missing = [c for c in critical if c not in awareness.containers]

            if missing:
                insights.append(SynapticInsight(
                    type=InsightType.ANOMALY,
                    priority=InsightPriority.CRITICAL,
                    title="Critical containers missing",
                    description=f"Missing: {', '.join(missing)}",
                    sources=["docker_health"],
                    evidence={"missing": missing},
                    action="docker compose up -d",
                ))

        except Exception as e:
            logger.debug(f"Container analysis failed: {e}")

        return insights

    def _analyze_patterns(self) -> List[SynapticInsight]:
        """Analyze architecture patterns for insights."""
        insights = []

        try:
            brain_state_path = self.memory_dir / "brain_state.md"
            if brain_state_path.exists():
                content = brain_state_path.read_text()

                # Check for active patterns
                if "deployment" in content.lower():
                    insights.append(SynapticInsight(
                        type=InsightType.OBSERVATION,
                        priority=InsightPriority.LOW,
                        title="Deployment pattern active",
                        description="Architecture brain shows deployment activity",
                        sources=["architecture_brain"],
                        evidence={"pattern": "deployment"},
                    ))

        except Exception as e:
            logger.debug(f"Pattern analysis failed: {e}")

        return insights

    def _analyze_alerts(self) -> List[SynapticInsight]:
        """Analyze health alerts for insights."""
        insights = []

        try:
            from memory.synaptic_health_alerts import HealthAlertSystem
            system = HealthAlertSystem()
            alerts = system.check_all_sources()

            for alert in alerts:
                severity = alert.severity if hasattr(alert, 'severity') else alert.get('severity', 'unknown')
                message = alert.message if hasattr(alert, 'message') else alert.get('message', '')
                source = alert.source if hasattr(alert, 'source') else alert.get('source', 'unknown')

                priority = InsightPriority.CRITICAL if severity == "critical" else InsightPriority.HIGH

                insights.append(SynapticInsight(
                    type=InsightType.ANOMALY,
                    priority=priority,
                    title=f"Health Alert: {source}",
                    description=message,
                    sources=["health_alerts"],
                    evidence={"alert": asdict(alert) if hasattr(alert, '__dataclass_fields__') else alert},
                ))

        except Exception as e:
            logger.debug(f"Alert analysis failed: {e}")

        return insights

    def _cross_correlate(self, insights: List[SynapticInsight]) -> List[SynapticInsight]:
        """Cross-correlate insights from multiple sources."""
        new_insights = []

        # Check for dialogue-health correlations
        dialogue_topics = [i for i in insights if "dialogue" in str(i.sources)]
        health_issues = [i for i in insights if "docker_health" in str(i.sources) and i.priority in [InsightPriority.HIGH, InsightPriority.CRITICAL]]

        if dialogue_topics and health_issues:
            new_insights.append(SynapticInsight(
                type=InsightType.CORRELATION,
                priority=InsightPriority.HIGH,
                title="Active work affected by system issues",
                description=f"Discussion topics may be affected by {len(health_issues)} health issues",
                sources=["dialogue_mirror", "docker_health"],
                evidence={
                    "topics": [i.title for i in dialogue_topics],
                    "issues": [i.title for i in health_issues],
                },
                action="Address health issues before continuing current work",
            ))

        return new_insights

    def _get_health_summary(self) -> Dict:
        """Get a quick health summary."""
        try:
            from memory.synaptic_observatory import quick_awareness
            return quick_awareness()
        except Exception:
            return {"error": "Could not get health summary"}


